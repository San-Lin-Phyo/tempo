#include <iostream>
using namespace std;

int main() {
    cout << endl;
                                    // relational operators
    cout << ( 5 > 6 ) << "\n";      // greater than   
    cout << ( 5 < 6 ) << "\n";      // less than   
    cout << ( 5 >= 6 ) << "\n";     // greater than or equal to  
    cout << ( 5 <= 6 ) << "\n";     // less than or equal to     
    cout << ( 5 == 6 ) << "\n";     // equivalent 
    cout << ( 5 != 6 ) << "\n";     // inequivalent   

    cout << endl;
    return 0;
}
